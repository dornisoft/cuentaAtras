    private void comenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comenzarActionPerformed
        // TODO add your handling code here:
        int horasIniciales;
        int minutosIniciales;
        try{
            
            if(horas.getText().length()==0){
                horasIniciales=0;
            }else{
                horasIniciales=Integer.parseInt(horas.getText());
            }
            if(minutos.getText().length()==0){
                minutosIniciales=0;
            }else{
                minutosIniciales=Integer.parseInt(minutos.getText());
            }
            
            
            
            int seg =(horasIniciales*60*60)+(minutosIniciales*60);
            System.out.println(seg);
        
            miReloj.setDetenido(false);
            miReloj.setFinalizado(false);
            miReloj.setSegundosIniciales(seg);
            miReloj.contar();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "No puedes introducir letras en los campos horas y minutos", "AVISO", JOptionPane.WARNING_MESSAGE);
        }
        
        
        
        
        
    }//GEN-LAST:event_comenzarActionPerformed

